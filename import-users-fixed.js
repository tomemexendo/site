// Função para extrair primeiro nome
function extractFirstName(fullName) {
  return fullName.trim().split(' ')[0].toLowerCase();
}

async function main() {
  console.log('Iniciando importação de usuários (apenas primeiro nome)...');
  
  for (const user of users) {
    try {
      const firstName = extractFirstName(user.name);
      await prisma.user.create({
        data: {
          name: firstName,
          phone: user.phone,
        },
      });
      console.log(`✓ ${firstName} importado`);
    } catch (error) {
      if (error.code === 'P2002') {
        console.log(`⚠ ${user.name} já existe`);
      } else {
        console.error(`✗ Erro ao importar ${user.name}:`, error.message);
      }
    }
  }
  
  console.log('Importação concluída!');
  await prisma.$disconnect();
}

main();
